public class Variable 
{
	public static void main(String[] args) 
    {
		int aValue1;                    // Indirectly assigning a value
		// Note that the variable is being started with a small letter, that is how naming convention for variable works in JAVA.
		aValue1=40;
		System.out.println(aValue1);
		
		int aValue2=10;                // Directly assigning a value
		System.out.println(aValue2);
	}
}