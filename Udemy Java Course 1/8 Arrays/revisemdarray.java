class revisemdarray
{
    public static void main(String[] args) 
    {
        int[][] m = {{10,20,30},{40,50,60},{70,80,90}};

        for(int array=0; array<m.length; array++)
        {
            for(int item=0; item<m[array].length; item++)
            {
                System.out.print(m[array][item] + "\t");
            }
            
            System.out.println();  

        // THIS WHOLE CODE IS VERY IMPORTANT !!!
        }
    }
}